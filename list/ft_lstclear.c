#include "../sorted.h"

void	ft_lstclear(t_list **lst)
{
	t_list	*p;

	if (!*lst)
	{
		free(lst);
		return ;
	}
	p = *lst;
	*lst = p->next;
	ft_lstclear(lst);
	free(p);
}
